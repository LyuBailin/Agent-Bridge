/**
 * Synthetic Results: Interrupt & Failure Recovery
 *
 * When a user interrupts a stream or a sibling tool in a parallel batch fails,
 * the system must generate synthetic results to "balance the ledger."
 *
 * This prevents the agent from acting on a corrupted premise or incomplete state.
 */

/**
 * Generate synthetic tool results for interrupted tool calls.
 * Used when streaming is interrupted or tool execution is cancelled.
 *
 * @param {Array} interruptedToolCalls - Tool calls that were interrupted
 * @param {string} reason - Human-readable reason (e.g., "Action cancelled by user")
 * @returns {Array} - Synthetic result objects matching expected format
 */
function yieldMissingToolResultBlocks(interruptedToolCalls, reason = "Action cancelled by user") {
  return interruptedToolCalls.map(toolCall => ({
    function: toolCall.function || toolCall,
    result: {
      success: false,
      error: reason,
      synthetic: true
    }
  }));
}

/**
 * Generate a synthetic error result for a single tool call.
 */
function createSyntheticError(toolCall, reason) {
  return {
    function: toolCall.function || toolCall,
    result: {
      success: false,
      error: reason,
      synthetic: true
    }
  };
}

/**
 * Check if a tool result is synthetic (generated by this module).
 */
function isSyntheticResult(result) {
  return result && result.synthetic === true;
}

/**
 * Handle sibling failure in a parallel batch.
 * When one critical tool fails, decide whether to continue or discard the batch.
 *
 * @param {Object} failedTool - The tool that failed
 * @param {Array} batch - All tools in the parallel batch
 * @param {Object} options - Configuration options
 * @returns {Object} - { action: "continue" | "discard", reason: string }
 */
function handleSiblingFailure(failedTool, batch, options = {}) {
  const {
    discardOnDestructive = true,  // Discard batch if destructive tool fails
    discardOnHighRisk = true       // Discard batch if HIGH/CRITICAL risk tool fails
  } = options;

  const toolName = failedTool.function?.name || failedTool.name;
  const errorReason = failedTool.result?.error || "Unknown error";

  // Define critical tools that should cause batch discard
  const criticalTools = ["rm", "mv"];

  if (criticalTools.includes(toolName)) {
    return {
      action: "discard",
      reason: `Critical tool '${toolName}' failed: ${errorReason}. Discarding entire batch.`
    };
  }

  if (failedTool.result?.risk === "critical" || failedTool.result?.risk === "high") {
    if (discardOnHighRisk) {
      return {
        action: "discard",
        reason: `High-risk tool '${toolName}' failed: ${errorReason}. Discarding entire batch.`
      };
    }
  }

  // Non-critical tool failure - continue with remaining tools
  return {
    action: "continue",
    reason: `Non-critical tool '${toolName}' failed but continuing with other tools.`
  };
}

/**
 * Determine if batch execution should continue or stop based on results.
 *
 * @param {Array} results - Array of tool execution results
 * @returns {Object} - { shouldContinue: boolean, criticalFailure: Object|null }
 */
function analyzeBatchResults(results) {
  let hasCriticalFailure = false;
  let criticalFailure = null;

  for (const result of results) {
    if (!result.result?.success && result.result?.synthetic !== true) {
      const toolName = result.function?.name || "unknown";

      // Check if this is a critical failure
      if (["rm", "mv"].includes(toolName)) {
        hasCriticalFailure = true;
        criticalFailure = result;
        break;
      }
    }
  }

  return {
    shouldContinue: !hasCriticalFailure,
    criticalFailure
  };
}

/**
 * Merge synthetic results with real results.
 * Ensures transcript has result for every tool call.
 *
 * @param {Array} toolCalls - Original tool calls
 * @param {Array} results - Actual results (may be incomplete)
 * @param {string} missingReason - Reason for missing results
 * @returns {Array} - Complete results with synthetic results for any missing
 */
function mergeResultsWithSynthetic(toolCalls, results, missingReason = "Result not received") {
  const resultMap = new Map();

  // Index real results by tool call ID or function name
  for (const result of results) {
    const key = result.function?.name || result.function?.arguments || JSON.stringify(result);
    resultMap.set(key, result);
  }

  const merged = [];

  for (const toolCall of toolCalls) {
    const key = toolCall.function?.name || toolCall.function?.arguments || JSON.stringify(toolCall);
    const existingResult = resultMap.get(key) || results.find(r =>
      r.function?.name === toolCall.function?.name &&
      r.function?.arguments === toolCall.function?.arguments
    );

    if (existingResult) {
      merged.push(existingResult);
    } else {
      merged.push(createSyntheticError(toolCall, missingReason));
    }
  }

  return merged;
}

module.exports = {
  yieldMissingToolResultBlocks,
  createSyntheticError,
  isSyntheticResult,
  handleSiblingFailure,
  analyzeBatchResults,
  mergeResultsWithSynthetic
};
