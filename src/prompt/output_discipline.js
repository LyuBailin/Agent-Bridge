    "           \"arguments\": {",
    "             \"file\": \"src/utils.js\",",
    "             \"search\": \"const add = (a, b) => a + b;\",",
    "             \"replace\": \"const add = (a, b) => {\\n  return a + b;\\n};\",",
    "           }",
    "         }",
    "       }",
    "     ]",
    "   }",
    "",
