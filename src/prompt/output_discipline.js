    "   - Output a single valid JSON object",
    "   - Structure: { \"tool_calls\": [{ \"function\": { \"name\": \"...\", \"arguments\": {...} } }] }",
    "   - arguments must be an object (not a string)",
    "   - File paths must be relative to workspace (no absolute paths)",
    "",
    "3. VALIDATION STANDARDS:",
    "   - All changes must be syntactically correct",
    "   - All paths must be valid and within the workspace",
    "   - All SEARCH patterns must match existing content exactly (when non-empty)",
    "   - All operations must be safe and reversible",
    "",
    "4. DENIED OPERATIONS:",
    "   - bash, npm, git, shell, exec, run, command - these are blocked",
